package org.java_18exceptionHandling;

public class TryCatchFinally {

	public static void main(String[] args) {
		//We can write try catch block in each block(i.e try ,Catch ,Finally)

		try {
			try {

			}catch(Exception e) {

			}
		}catch(Exception e) {

			try {

			}catch(Exception j) {

			}
		}finally {
			try {

			}catch(Exception j) {

			}
		}

	}

}
